package com.example.allinonescore.Data;

public class OverData {

    private int run;
    private String check;



    public OverData() {
    }

    public OverData(int run,String check) {
        this.run = run;
        this.check =check;
    }

    public int getRun() {
        return run;
    }

    public void setRun(int run) {
        this.run = run;
    }

    public String getCheck() {
        return check;
    }

    public void setCheck(String check) {
        this.check = check;
    }
}
